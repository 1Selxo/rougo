}

private fun YoutubeStreamFormat.bestPlaybackUrl(): String? {
    val manifest = manifestUrl?.takeIf { it.startsWith("http", ignoreCase = true) }
    val directUrl = url?.takeIf { it.startsWith("http", ignoreCase = true) }
    val protocolValue = protocol.orEmpty().lowercase(Locale.US)
    return when {
        protocolValue.contains("m3u8") -> directUrl ?: manifest
        protocolValue.contains("dash") -> manifest ?: directUrl
        isVlcFriendlyVideoFormat() || isVlcFriendlyAudioFormat() -> directUrl ?: manifest
        else -> manifest ?: directUrl
    }
}

fun YoutubeStreamFormat.isVlcFriendlyVideoFormat(): Boolean {
    val codec = vcodec.orEmpty().lowercase(Locale.US)
    val audioCodec = acodec.orEmpty().lowercase(Locale.US)
    val extension = ext.orEmpty().lowercase(Locale.US)
    if (codec == "none" || audioCodec == "none") return false
    return extension == "mp4" &&
        (codec.startsWith("avc") || codec.startsWith("h264")) &&
        (audioCodec.startsWith("mp4a") || audioCodec.startsWith("aac"))
}

fun YoutubeStreamFormat.isVlcFriendlyAudioFormat(): Boolean {
    val codec = acodec.orEmpty().lowercase(Locale.US)
    val extension = ext.orEmpty().lowercase(Locale.US)
    val videoCodec = vcodec.orEmpty().lowercase(Locale.US)
    if (videoCodec != "none" || codec == "none") return false
    return extension == "m4a" || extension == "mp4" || codec.startsWith("mp4a") || codec.startsWith("aac")
}

private fun isVideoFile(file: File): Boolean {
    return file.extension.lowercase(Locale.US) in setOf("mp4", "mkv", "webm", "mov", "avi", "m4v")
}

data class CachedStreamUrl(val url: String, val expiresAtMillis: Long)

enum class StreamProvider { YouTube, Bilibili, Niconico, Other }

object StreamUrlCache {
    private val values = mutableMapOf<String, CachedStreamUrl>()
    fun get(key: String): CachedStreamUrl? = values[key]
    fun put(key: String, value: CachedStreamUrl) {
        values[key] = value
    }
    fun remove(key: String) {
        values.remove(key)
    }
}

fun cachedPlayableMediaUri(item: LibraryItem): String? {
    val sourceUrl = item.sourceUrl ?: return null
    return cachedResolvedStreamUrl(sourceUrl, item.formatId)
}

fun cachedResolvedStreamUrl(sourceUrl: String, formatId: String?): String? {
    val now = System.currentTimeMillis()
    val cached = StreamUrlCache.get(streamUrlCacheKey(sourceUrl, formatId))
        ?: return null
    if (cached.expiresAtMillis <= now) {
        StreamUrlCache.remove(streamUrlCacheKey(sourceUrl, formatId))
        return null
    }
    if (isYoutubeUrl(sourceUrl) && isExpiredYoutubeDirectStreamUrl(cached.url, now)) {
        StreamUrlCache.remove(streamUrlCacheKey(sourceUrl, formatId))
        return null
    }
    return cached.url
}

fun cacheResolvedStreamUrl(sourceUrl: String?, formatId: String?, streamUrl: String?) {
    val source = sourceUrl?.trim()?.takeIf { it.isNotBlank() } ?: return
    val stream = streamUrl?.trim()?.takeIf { it.startsWith("http", ignoreCase = true) } ?: return
    if (stream == source) return
    val now = System.currentTimeMillis()
    if (isYoutubeUrl(source) && isExpiredYoutubeDirectStreamUrl(stream, now)) return
    val provider = detectStreamProvider(source)
    val expiresAt = streamUrlCacheExpiryMs(provider, now, stream)
    StreamUrlCache.put(streamUrlCacheKey(source, formatId), CachedStreamUrl(stream, expiresAt))
}

private fun streamUrlCacheKey(sourceUrl: String, formatId: String?): String {
    val resolvedFormatId = formatId.takeUnless { isKnownYoutubeVideoOnlyFormatId(it) }
    return "stream_cache_${sourceUrl}_${resolvedFormatId.orEmpty()}"
}

fun detectStreamProvider(sourceUrl: String): StreamProvider = when {
    isYoutubeUrl(sourceUrl) -> StreamProvider.YouTube
    isBilibiliUrl(sourceUrl) -> StreamProvider.Bilibili
    isNiconicoUrl(sourceUrl) -> StreamProvider.Niconico
    else -> StreamProvider.Other
}

fun streamUrlCacheExpiryMs(provider: StreamProvider, nowMs: Long): Long {
    val ttl = when (provider) {
        StreamProvider.YouTube -> 4 * 60 * 60 * 1000L
        StreamProvider.Bilibili -> 2 * 60 * 60 * 1000L
        StreamProvider.Niconico -> 2 * 60 * 60 * 1000L
        StreamProvider.Other -> 30 * 60 * 1000L
    }
    return nowMs + ttl
}

private fun streamUrlCacheExpiryMs(provider: StreamProvider, nowMs: Long, streamUrl: String): Long {
    val defaultExpiry = streamUrlCacheExpiryMs(provider, nowMs)
    val directUrlExpiry = youtubeDirectStreamExpiryMs(streamUrl)?.minus(60_000L)
    return if (provider == StreamProvider.YouTube && directUrlExpiry != null) {
        minOf(defaultExpiry, directUrlExpiry)
    } else {
        defaultExpiry
    }
}

fun isExpiredYoutubeDirectStreamUrl(streamUrl: String, nowMs: Long = System.currentTimeMillis()): Boolean {
    val expiry = youtubeDirectStreamExpiryMs(streamUrl) ?: return false
    return expiry <= nowMs + 60_000L
}

private fun youtubeDirectStreamExpiryMs(streamUrl: String): Long? {
    val match = Regex("""[?&]expire=(\d+)""").find(streamUrl) ?: return null
    val seconds = match.groupValues.getOrNull(1)?.toLongOrNull() ?: return null
    return seconds * 1000L
}
