object MediaTools {
    private val toolsDir = File(WindowsPaths.appDir, "tools").also { it.mkdirs() }
    private val ytdlpExe = File(toolsDir, "yt-dlp.exe")

    suspend fun ensureReady(onStatus: (String) -> Unit = {}): Boolean = withContext(Dispatchers.IO) {
        try {
            ensureYtDlp(onStatus)
            if (ffmpegCommand() == null || ffprobeCommand() == null) {
                onStatus("Bundled FFmpeg was not found. Waveform and metadata extraction need the packaged build.")
            }
            true
        } catch (t: Throwable) {
            CrashReporter.recordHandled("MediaTools.ensureReady", t)
            false
        }
    }

    fun ytDlpCommand(): String? =
        bundledTool("yt-dlp.exe")?.absolutePath
            ?: ytdlpExe.takeIf { it.isFile }?.absolutePath
            ?: findOnPath("yt-dlp.exe")
            ?: findOnPath("yt-dlp")

    fun ffmpegCommand(): String? =
        bundledTool("ffmpeg/ffmpeg.exe")?.absolutePath ?: findOnPath("ffmpeg.exe") ?: findOnPath("ffmpeg")

    fun ffprobeCommand(): String? =
        bundledTool("ffmpeg/ffprobe.exe")?.absolutePath ?: findOnPath("ffprobe.exe") ?: findOnPath("ffprobe")

    fun rnnoiseModelFile(): File? =
        bundledTool("rnnoise/std.rnnn")?.takeIf { it.isFile }

    fun vlcNativeDir(): File? =
        bundledTool("vlc/libvlc.dll")?.parentFile
            ?: File("C:/Program Files/VideoLAN/VLC").takeIf { File(it, "libvlc.dll").isFile }
            ?: File("C:/Program Files (x86)/VideoLAN/VLC").takeIf { File(it, "libvlc.dll").isFile }

    private fun ensureYtDlp(onStatus: (String) -> Unit) {
        if (bundledTool("yt-dlp.exe")?.isFile == true) return
        val pathCommand = findOnPath("yt-dlp.exe") ?: findOnPath("yt-dlp")
        if (pathCommand != null && !ytdlpExe.isFile) return

        val lastUpdate = Prefs.app.getLong("last_ytdlp_update", 0L)
        val shouldDownload = !ytdlpExe.isFile || System.currentTimeMillis() - lastUpdate > 24 * 60 * 60 * 1000L
        if (!shouldDownload) return

        onStatus("Downloading yt-dlp...")
        val tmp = File(toolsDir, "yt-dlp.exe.tmp")
        val connection = URL("https://github.com/yt-dlp/yt-dlp/releases/latest/download/yt-dlp.exe")
            .openConnection() as HttpURLConnection
        connection.requestMethod = "GET"
        connection.connectTimeout = 30_000
        connection.readTimeout = 120_000
        connection.inputStream.use { input ->
            tmp.outputStream().use { output -> input.copyTo(output) }
        }
        Files.move(tmp.toPath(), ytdlpExe.toPath(), StandardCopyOption.REPLACE_EXISTING)
        Prefs.app.putLong("last_ytdlp_update", System.currentTimeMillis())
        onStatus("")
    }

    private fun bundledTool(relativePath: String): File? {
        val root = System.getProperty("compose.application.resources.dir")
            ?.takeIf { it.isNotBlank() }
            ?.let(::File)
            ?: return null
        return File(root, "tools/$relativePath").takeIf { it.isFile }
    }

    private fun findOnPath(name: String): String? {
        val path = System.getenv("PATH") ?: return null
        val separator = File.pathSeparator
        return path.split(separator)
            .asSequence()
            .map { File(it, name) }
            .firstOrNull { it.isFile && it.canExecute() }
            ?.absolutePath
    }
}

fun runProcess(command: List<String>, timeoutSeconds: Long = 120): ProcessResult {
    return try {
        val process = ProcessBuilder(command)
            .redirectErrorStream(true)
            .start()
        val outputFuture = CompletableFuture.supplyAsync {
            process.inputStream.bufferedReader(Charsets.UTF_8).use { it.readText() }
        }
        val completed = process.waitFor(timeoutSeconds, TimeUnit.SECONDS)
        if (!completed) {
            process.destroyForcibly()
            ProcessResult(-1, outputFuture.get(2, TimeUnit.SECONDS), timedOut = true)
        } else {
            ProcessResult(process.exitValue(), outputFuture.get(2, TimeUnit.SECONDS))
        }
    } catch (t: Throwable) {
        ProcessResult(-1, t.message.orEmpty())
    }
}

fun playableYoutubeUrl(url: String): String? {
    val uri = runCatching { URI(url) }.getOrNull() ?: return null
    val host = uri.host.orEmpty().lowercase(Locale.US)
    val segments = uri.path.orEmpty().trim('/').split('/').filter { it.isNotBlank() }
    val hasVideoId = when {
        host == "youtu.be" || host.endsWith(".youtu.be") -> segments.firstOrNull()?.isNotBlank() == true
        host.contains("youtube.com") -> {
            uri.rawQuery.orEmpty().split('&').any { it.substringBefore('=') == "v" && it.substringAfter('=', "").isNotBlank() } ||
                (segments.size >= 2 && segments[0] in setOf("shorts", "live", "embed") && segments[1].isNotBlank())
        }
        else -> false
    }
    return if (hasVideoId) url else null
}

fun youtubeThumbnailUrl(sourceUrl: String?): String? {
    val id = youtubeVideoId(sourceUrl) ?: return null
    return "https://img.youtube.com/vi/$id/hqdefault.jpg"
}

private fun youtubeVideoId(sourceUrl: String?): String? {
    if (sourceUrl.isNullOrBlank()) return null
    val uri = runCatching { URI(sourceUrl) }.getOrNull() ?: return null
    val host = uri.host.orEmpty().lowercase(Locale.US)
    val segments = uri.path.orEmpty().trim('/').split('/').filter { it.isNotBlank() }
    return when {
        host.contains("youtu.be") -> segments.firstOrNull()
        host.contains("youtube.com") -> {
            uri.rawQuery.orEmpty().split('&').firstOrNull { it.substringBefore('=') == "v" }?.substringAfter('=', "")
                ?: if (segments.size >= 2 && segments[0] in setOf("shorts", "live", "embed")) segments[1] else null
        }
        else -> null
    }?.takeIf { it.isNotBlank() }
}

