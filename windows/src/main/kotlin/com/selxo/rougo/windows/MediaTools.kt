fun resolveYoutubeMediaStream(
    url: String,
    formatId: String?,
    preferredResolution: String = DEFAULT_YOUTUBE_RESOLUTION
): ResolvedMediaStream? {
    val resolvedFormatId = formatId.takeUnless { isKnownYoutubeVideoOnlyFormatId(it) }
    cachedResolvedStreamUrl(url, resolvedFormatId)?.let { return ResolvedMediaStream(it) }
    val command = MediaTools.ytDlpCommand() ?: return null
    val args = buildList {
        add(command)
        add("--no-playlist")
        resolvedFormatId?.takeIf { it.isNotBlank() }?.let {
            add("-f")
            add(it)
        } ?: run {
            add("-f")
            add(fastYoutubeFormatSelector(preferredResolution))
        }
        add("--dump-json")
        add(url)
    }
    val result = runProcess(args, timeoutSeconds = 90)
    if (result.exitCode != 0) return null
    val json = runCatching { JSONObject(extractDumpedJson(result.output)) }.getOrNull() ?: return null
    val headers = parseStreamHttpHeaders(json)
    val formats = parseYoutubeFormats(json)
    val selected = resolvedFormatId
        ?.let { id -> formats.firstOrNull { it.formatId == id } }
        ?: selectPreferredYoutubeFormat(formats, preferredResolution)
    val resolved = json.optString("url").takeIf { it.startsWith("http", ignoreCase = true) }
        ?: selected?.bestPlaybackUrl()
        ?: return null
    cacheResolvedStreamUrl(url, resolvedFormatId, resolved)
    return ResolvedMediaStream(resolved, headers.first, headers.second)
}

fun invalidateResolvedStreamUrl(url: String, formatId: String?) {
    val resolvedFormatId = formatId.takeUnless { isKnownYoutubeVideoOnlyFormatId(it) }
    StreamUrlCache.remove(streamUrlCacheKey(url, resolvedFormatId))
}

fun downloadYoutubeSubtitle(url: String, languageCode: String, isAutoGenerated: Boolean): String? {
    if (!isYoutubeUrl(url)) return null
    val command = MediaTools.ytDlpCommand() ?: return null
    val fileId = UUID.randomUUID().toString().replace(Regex("[^A-Za-z0-9_-]"), "_")
    val outputTemplate = File(WindowsPaths.subtitlesDir, "$fileId.%(ext)s").absolutePath
    val args = buildList {
        add(command)
        add("--no-playlist")
        add("--skip-download")
        if (isAutoGenerated) add("--write-auto-subs") else add("--write-subs")
        add("--sub-langs")
        add(languageCode)
        add("--sub-format")
        add("vtt/srt/best")
        add("-o")
        add(outputTemplate)
        add(url)
    }
    val result = runProcess(args, timeoutSeconds = 90)
    if (result.exitCode != 0) {
        return if (!isAutoGenerated) downloadYoutubeSubtitle(url, languageCode, true) else null
    }
    val subtitle = WindowsPaths.subtitlesDir.listFiles()
        ?.filter { it.isFile && it.name.startsWith(fileId) && it.extension.lowercase(Locale.US) in setOf("vtt", "srt", "ass") }
        ?.maxByOrNull { it.lastModified() }
        ?.absolutePath
    return subtitle ?: if (!isAutoGenerated) downloadYoutubeSubtitle(url, languageCode, true) else null
}

fun downloadVideoLinkToLibraryItem(
    url: String,
    existingItem: LibraryItem? = null,
    onProgress: (String) -> Unit = {}
): LibraryItem? {
    val command = MediaTools.ytDlpCommand() ?: return null
    val itemId = existingItem?.id ?: UUID.randomUUID().toString()
    val fileId = itemId.replace(Regex("[^A-Za-z0-9_-]"), "_")
    val outputTemplate = File(WindowsPaths.downloadsDir, "$fileId.%(ext)s").absolutePath
    onProgress("Downloading ${streamSourceLabel(url)} video...")
    val result = runProcess(
        listOf(
            command,
            "--no-playlist",
            "--newline",
            "--embed-metadata",
            "--write-thumbnail",
            "-f",
            "bestvideo[height<=720]+bestaudio/best[height<=720]/best",
            "--merge-output-format",
            "mp4",
            "-o",
            outputTemplate,
            url
        ),
        timeoutSeconds = 60 * 60
    )
    if (result.exitCode != 0) {
        onProgress("${streamSourceLabel(url)} download failed.")
        DesktopNotifier.warning("Download failed", "${streamSourceLabel(url)} download failed.")
        return null
    }
    val mediaFile = WindowsPaths.downloadsDir.listFiles()
        ?.filter { it.isFile && it.name.startsWith(fileId) && it.extension.lowercase(Locale.US) !in setOf("jpg", "jpeg", "png", "webp", "vtt", "srt") }
        ?.maxByOrNull { it.lastModified() }
        ?: return null
    val metadata = extractMediaMetadata(mediaFile.absolutePath, itemId, isVideoFile(mediaFile))
    val title = cleanMetadataValue(metadata.title) ?: existingItem?.title ?: mediaFile.nameWithoutExtension
    val cover = metadata.coverArtPath ?: WindowsPaths.downloadsDir.listFiles()
        ?.firstOrNull { it.isFile && it.name.startsWith(fileId) && it.extension.lowercase(Locale.US) in setOf("jpg", "jpeg", "png", "webp") }
        ?.absolutePath
    onProgress("")
    DesktopNotifier.info("Download complete", title)
    return (existingItem ?: LibraryItem(
        id = itemId,
        title = title,
        mediaUri = mediaFile.absolutePath,
        subtitleUri = null,
        progress = 0L,
        duration = metadata.durationMs ?: 0L,
        isVideo = isVideoFile(mediaFile),
        sourceUrl = url,
        coverArtPath = cover
    )).copy(
        title = title,
        mediaUri = mediaFile.absolutePath,
        duration = metadata.durationMs ?: existingItem?.duration ?: 0L,
        isVideo = isVideoFile(mediaFile),
        sourceUrl = existingItem?.sourceUrl ?: url,
        coverArtPath = cover ?: existingItem?.coverArtPath
    )
}

fun initialPlayableMediaUri(item: LibraryItem): String? {
    if (item.hasLegacyYoutubeVideoOnlyFormat()) return null
    val mediaUri = item.mediaUri.trim()
    if (mediaUri.isBlank()) return null
    if (item.sourceUrl == null) return mediaUri
    if (item.hasDownloadedLocalCopy()) return item.mediaUri
    if (mediaUri != item.sourceUrl.trim()) {
        if (isYoutubeUrl(item.sourceUrl) && isExpiredYoutubeDirectStreamUrl(mediaUri)) return null
        return mediaUri
    }
    return null
}

fun extractMediaMetadata(path: String, itemId: String, isVideo: Boolean): MediaMetadataSnapshot {
    val ffprobe = MediaTools.ffprobeCommand()
    if (ffprobe != null) {
        val result = runProcess(
            listOf(ffprobe, "-v", "quiet", "-print_format", "json", "-show_format", "-show_streams", path),
            timeoutSeconds = 30
        )
        if (result.exitCode == 0 && result.output.isNotBlank()) {
            runCatching {
                val json = JSONObject(result.output)
                val format = json.optJSONObject("format") ?: JSONObject()
                val tags = format.optJSONObject("tags") ?: JSONObject()
                val durationMs = format.optDouble("duration", 0.0).takeIf { it > 0.0 }?.times(1000.0)?.roundToInt()?.toLong()
                val coverPath = extractAttachedPictureWithFfmpeg(path, itemId)
                return MediaMetadataSnapshot(
                    title = tags.optCleanString("title"),
                    artist = tags.optCleanString("artist"),
                    album = tags.optCleanString("album"),
                    albumArtist = tags.optCleanString("album_artist") ?: tags.optCleanString("albumartist"),
                    genre = tags.optCleanString("genre"),
                    year = tags.optCleanString("date") ?: tags.optCleanString("year"),
                    durationMs = durationMs,
                    coverArtPath = coverPath
                )
            }.onFailure {
                CrashReporter.recordHandled("extractMediaMetadata.ffprobe", it)
            }
        }
    }
    return MediaMetadataSnapshot(
        title = File(path).nameWithoutExtension,
        durationMs = null,
        coverArtPath = if (isVideo) null else null
    )
}

private fun extractAttachedPictureWithFfmpeg(path: String, itemId: String): String? {
    val ffmpeg = MediaTools.ffmpegCommand() ?: return null
    val target = File(WindowsPaths.coversDir, "${itemId.replace(Regex("[^A-Za-z0-9_-]"), "_")}.jpg")
    val result = runProcess(
        listOf(ffmpeg, "-y", "-i", path, "-an", "-vcodec", "mjpeg", "-frames:v", "1", target.absolutePath),
        timeoutSeconds = 30
    )
    return target.takeIf { result.exitCode == 0 && it.isFile && it.length() > 0L }?.absolutePath
}

