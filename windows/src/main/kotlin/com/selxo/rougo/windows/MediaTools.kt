fun fetchFastYoutubeStream(url: String, preferredResolution: String): FastYoutubeStream? {
    val command = MediaTools.ytDlpCommand() ?: return null
    val format = fastYoutubeFormatSelector(preferredResolution)
    val result = runProcess(
        listOf(command, "--no-playlist", "--dump-json", "-f", format, url),
        timeoutSeconds = 90
    )
    if (result.exitCode != 0) return null
    val json = JSONObject(extractDumpedJson(result.output))
    val formats = parseYoutubeFormats(json)
    val selected = selectPreferredYoutubeFormat(formats, preferredResolution)
    val streamUrl = selected?.bestPlaybackUrl() ?: json.optString("url").takeIf { it.isNotBlank() } ?: return null
    val headers = parseStreamHttpHeaders(json)
    return FastYoutubeStream(
        title = cleanYtdlpTitle(json.optString("title"), url) ?: sourceDefaultTitle(url),
        streamUrl = streamUrl,
        formatId = selected?.formatId ?: json.optString("format_id").takeIf { it.isNotBlank() },
        isVideo = selected?.isVlcFriendlyVideoFormat() ?: true,
        httpUserAgent = headers.first,
        httpReferer = headers.second
    )
}

