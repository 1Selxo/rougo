fun parseYoutubePlaylistImportData(json: JSONObject): YoutubePlaylistImportData {
    val title = firstCleanMetadataValue(
        cleanMetadataValue(json.optString("title")),
        cleanMetadataValue(json.optString("playlist_title"))
    ) ?: "Playlist"
    val entriesJson = json.optJSONArray("entries") ?: JSONArray()
    val entries = buildList {
        for (i in 0 until entriesJson.length()) {
            val entry = entriesJson.optJSONObject(i) ?: continue
            val sourceUrl = youtubePlaylistEntrySourceUrl(entry) ?: continue
            add(
                PlaylistImportEntry(
                    title = firstCleanMetadataValue(
                        cleanMetadataValue(entry.optString("title")),
                        cleanMetadataValue(entry.optString("fulltitle"))
                    ) ?: "Video ${size + 1}",
                    sourceUrl = sourceUrl,
                    thumbnailUrl = bestThumbnailUrl(entry) ?: youtubeThumbnailUrl(sourceUrl),
                    durationMs = entry.optLong("duration", 0L).takeIf { it > 0L }?.times(1000L) ?: 0L,
                    index = entry.optInt("playlist_index", i)
                )
            )
        }
    }
    return YoutubePlaylistImportData(title, entries.distinctBy { it.sourceUrl })
}

private fun youtubePlaylistEntrySourceUrl(entry: JSONObject): String? {
    val directUrl = entry.optString("url").takeIf { it.startsWith("http", ignoreCase = true) }
    if (directUrl != null) return directUrl
    val webpageUrl = entry.optString("webpage_url").takeIf { it.startsWith("http", ignoreCase = true) }
    if (webpageUrl != null) return webpageUrl
    val id = entry.optString("id").takeIf { it.isNotBlank() } ?: return null
    return "https://www.youtube.com/watch?v=$id"
}

