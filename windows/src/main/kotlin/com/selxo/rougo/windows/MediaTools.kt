private fun bestThumbnailUrl(json: JSONObject): String? {
    val thumbnails = json.optJSONArray("thumbnails") ?: return json.optString("thumbnail").takeIf { it.isNotBlank() }
    var best: JSONObject? = null
    for (i in 0 until thumbnails.length()) {
        val candidate = thumbnails.optJSONObject(i) ?: continue
        if (candidate.optString("url").isBlank()) continue
        if (best == null || candidate.optInt("width", 0) * candidate.optInt("height", 0) > best!!.optInt("width", 0) * best!!.optInt("height", 0)) {
            best = candidate
        }
    }
    return best?.optString("url")?.takeIf { it.isNotBlank() } ?: json.optString("thumbnail").takeIf { it.isNotBlank() }
}

private fun extractDumpedJson(output: String): String {
    val trimmed = output.trim()
    if (trimmed.startsWith("{")) return trimmed
    val start = trimmed.indexOf('{')
    val end = trimmed.lastIndexOf('}')
    return if (start >= 0 && end > start) trimmed.substring(start, end + 1) else trimmed
}

private fun parseYoutubeSubtitleChoices(json: JSONObject): List<YoutubeSubtitleChoice> {
    val choices = mutableListOf<YoutubeSubtitleChoice>()
    fun appendChoices(container: JSONObject?, auto: Boolean) {
        if (container == null) return
        container.keys().forEach { language ->
            val arr = container.optJSONArray(language) ?: return@forEach
            if (arr.length() == 0) return@forEach
            choices += YoutubeSubtitleChoice(
                label = if (auto) "$language (auto)" else language,
                languageCode = language,
                isAutoGenerated = auto
            )
        }
    }
    appendChoices(json.optJSONObject("subtitles"), false)
    appendChoices(json.optJSONObject("automatic_captions"), true)
    return choices.distinctBy { "${it.languageCode}:${it.isAutoGenerated}" }
}

fun parseYoutubeSubtitleListOutput(output: String): List<YoutubeSubtitleChoice> {
    val choices = mutableListOf<YoutubeSubtitleChoice>()
    var inSubtitleSection = false
    var automatic = false
    val languageCodePattern = Regex("""^[a-zA-Z]{2,3}(?:[-_][a-zA-Z0-9]+)*$""")

    output.lineSequence()
        .map { it.trim() }
        .filter { it.isNotBlank() }
        .forEach { line ->
            val lower = line.lowercase(Locale.US)
            when {
                lower.contains("available automatic captions") -> {
                    inSubtitleSection = true
                    automatic = true
                    return@forEach
                }
                lower.contains("available subtitles") -> {
                    inSubtitleSection = true
                    automatic = false
                    return@forEach
                }
                lower.startsWith("[info]") && !lower.contains("available") -> return@forEach
                lower.startsWith("language") || lower.startsWith("name") -> return@forEach
                !inSubtitleSection -> return@forEach
            }

            val code = line.split(Regex("""\s+""")).firstOrNull()
                ?.takeIf { languageCodePattern.matches(it) }
                ?: return@forEach
            choices += YoutubeSubtitleChoice(
                label = if (automatic) "$code (auto)" else code,
                languageCode = code,
                isAutoGenerated = automatic
            )
        }

    return choices.distinctBy { "${it.languageCode}:${it.isAutoGenerated}" }
}

private fun parseYoutubeFormats(json: JSONObject): List<YoutubeStreamFormat> {
